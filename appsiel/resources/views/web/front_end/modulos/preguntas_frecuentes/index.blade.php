      <button class="accordion"> <b> ¿Qué es software en la nube? </b> <a href="#" class="close" data-dismiss="alert" aria-label="close">&plus;</a></button>
      <div class="panel_acordion">
        <p> La nube es un conjunto de lugares físicos interconectados donde se procesa y almacena toda la información de la Internet. <br>
La nube es una metáfora utilizada para describir al conjunto de centros de datos (data centers) y servidores interconectados alrededor del mundo a través de Internet. Cuando se accede a Internet, siempre se está accediendo a uno o a miles de computadores físicos que brindan servicios informáticos.
 </p>
      </div>

      <button class="accordion"> <b> ¿Qué es una aplicación web? </b> <a href="#" class="close" data-dismiss="alert" aria-label="close">&plus;</a></button>
      <div class="panel_acordion">
        <p> Aplicación es sinónimo de software o programa informático. De este modo, una aplicación web es un programa informático que funciona en una navegador web como Google Chrome, Internet Explorer, Mozilla o Safari. No requiere instalación ni ejecutarse en un computador específico, sino que se puede acceder desde cualquier dispositivo. </p>
      </div>

      <button class="accordion"> <b> ¿Qué quiere decir “aplicaciones web individuales”? </b> <a href="#" class="close" data-dismiss="alert" aria-label="close">&plus;</a></button>
      <div class="panel_acordion">
        <p> Significa que cada aplicación es independiente una de la otra y que se pueden contratar según las necesidades de la empresa. No se manejan paquetes predefinidos. Solo algunas aplicaciones dependen estrictamente de otras. </p>
      </div>

      <button class="accordion"> <b> ¿Por qué es un servicio contratado? </b> <a href="#" class="close" data-dismiss="alert" aria-label="close">&plus;</a></button>
      <div class="panel_acordion">
        <p> Anteriormente solo se podía utilizar software empresarial a través de la compra de licencias para ser instalado en equipos o servidores propios de la empresa; y luego seguir pagando por actualizaciones y soporte técnico. <br>
Las nuevas tecnologías de la información permiten a la empresas contratar las herramientas de software como una suscripción. De este modo pueden ahorrar en gastos de licencias, equipos de computación y personal técnico para el funcionamiento, mantenimiento y actualización de las aplicaciones.
 </p>
      </div>

      <button class="accordion"> <b> ¿Si pago por las aplicaciones, el software es mio? </b> <a href="#" class="close" data-dismiss="alert" aria-label="close">&plus;</a></button>
      <div class="panel_acordion">
        <p> No. Un paquete de software se puede dividir en dos componentes: el código fuente y el programa ejecutable.
Por un lado, las aplicaciones web no requieren programa ejecutable. <br>
Por otro lado, el código fuente de APPSIEL es desarrollado y mantenido por la empresa APPSIEL S.A.S. y sólo pertenece a ella.<br>
Usted solo paga por el uso de las aplicaciones; esto incluye soporte técnico, mantenimiento y actualizaciones permanentes sin costo adicional.
 </p>
      </div>

      <button class="accordion"> <b> ¿Cuanto cuesta el servicio? </b> <a href="#" class="close" data-dismiss="alert" aria-label="close">&plus;</a></button>
      <div class="panel_acordion">
        <p> El servicio se liquida mensualmente y el costo se calcula de acuerdo al <b>número de aplicaciones</b> y al <b>número de usuarios</b> contratados.<br>
El costo final va a depender del tamaño y las necesidades específicas de cada empresa.
El pago se hace anticipado de manera trimestral, semestral o anual.
Ahora mismo APPSIEL tiene 15 aplicaciones para empresas de distintos sectores. Cada aplicación tiene un costo fijo y cada usuario adicional también tiene un costo fijo. <a href="#pricing">Ver precios</a> </p>
      </div>

      <button class="accordion"> <b> ¿Cómo se cuenta un usuario? </b> <a href="#" class="close" data-dismiss="alert" aria-label="close">&plus;</a></button>
      <div class="panel_acordion">
        <p> Un usuario es aquel que tiene acceso al uso de las aplicaciones a través de un Identificador de usuario y una Contraseña. Un usuario puede crear y visualizar las transacciones del sistema según los permisos y restricciones que se le asigne. <br>
Los terceros que se usan en las transacciones del sistema como clientes, proveedores, estudiantes, residentes, empleados, etc. no se cuentan como usuario.
 </p>
      </div>

